
public class EjercicioIfElse {
   public static void main(String args[]) {
        var numero = 2;
        var texto="Desconocido";
        if (numero==1) {
            texto = "uno"; 
        } else if(numero==2) {
            texto = "dos" ;
        }
        System.out.println("texto = " + texto);
    }           
}
